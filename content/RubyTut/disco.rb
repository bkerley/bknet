# disco.rb
class Disco
  
  def initialize
    #constructor
    @dancers = Array.new
    @currentsong = "Stayin\' Alive"
  end
  
  def to_s
    return "#{@currentsong} playing to: #{@dancers}"
  end
  
  def song=(newsong)
    @currentsong = newsong
  end
  
  def <<(dancer)
    @dancers << dancer
  end
end